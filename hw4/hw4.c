#include <stdio.h>

int main() {
    printf("九九乘法表\n");
    printf("========================================\n");
    
    // 外层循环控制行数（被乘数）
    for (int i = 1; i <= 9; i++) {
        // 内层循环控制列数（乘数），只打印到当前行数
        for (int j = 1; j <= i; j++) {
            // 计算乘积并格式化输出
            printf("%d×%d=%2d  ", j, i, j * i);
        }
        // 每行结束后换行
        printf("\n");
    }
    
    printf("========================================\n");
    return 0;
}